#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*ps1;
	unsigned char	*ps2;

	ps1 = (unsigned char *)s1;
	ps2 = (unsigned char *)s2;
	while (n--)
		if (*ps1++ != *ps2++)
			return (*(ps1 - 1) - *(ps2 - 1));
	return (0);
}
